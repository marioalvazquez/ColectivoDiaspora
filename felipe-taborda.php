            $(".estudiante").fadeIn();
            $(".profesionista").fadeOut();
            $("#institucion").val("");
            $("#empresa").val("No Aplica");

          }
          else if ($selection == "Profesionista") {
            $(".profesionista").fadeIn();
            $(".estudiante").fadeOut();
            $("#institucion").val("No Aplica");
            $("#empresa").val("");
          }
          else{
            $(".estudiante, .profesionista").fadeOut();
          }
        })
